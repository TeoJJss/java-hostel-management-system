    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = userTable.getSelectedRow();
        if (selectedRowIndex != -1){
            String[] selectedUser = userList[selectedRowIndex];
            String selectUsername = selectedUser[0];
            
            Manager manager = new Manager(username);
            try{
                int result = JOptionPane.showConfirmDialog(this,"Confirm Deletion?");
                if (result == JOptionPane.YES_OPTION){
                        manager.dltEndUser(selectUsername);
                        JOptionPane.showMessageDialog(this,"User has been deleted successfully");
                        displayUsers();

                }
            }catch(Exception e){
                System.out.println(e);
            }
        }else{
            System.out.println("Please select a user to delete");
            JOptionPane.showMessageDialog(this, "Please select a user to delete", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

