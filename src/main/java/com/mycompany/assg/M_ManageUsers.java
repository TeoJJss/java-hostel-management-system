    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = userTable.getSelectedRow();
        if (selectedRowIndex != -1){
            String[] selectedUser = userList[selectedRowIndex];
            String usernameS = selectedUser[0];
            String passwordS = selectedUser[1];
            String roleS = selectedUser[2];
            M_EditUser edit = new M_EditUser(usernameS,passwordS,roleS,username);
            edit.setVisible(true);
            dispose();
        }else{
            System.out.println("Please select a user first");
            JOptionPane.showMessageDialog(this, "Please select a user first", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editButtonActionPerformed

